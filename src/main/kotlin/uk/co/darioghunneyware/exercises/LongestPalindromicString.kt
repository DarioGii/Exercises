package uk.co.darioghunneyware.exercises

class LongestPalindromicString {
    fun longestPalindrome(s: String): String {
        var start = 0
        var end = 1
        var longestPalindrome = ""

        while (start < s.length) {
            while (end < s.length) {
            }
            start++
        }

        return longestPalindrome
    }
}
